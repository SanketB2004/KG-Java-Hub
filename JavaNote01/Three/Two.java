package JavaNote01.Three;

public class Two {

    public static void main(String[] args) {
       
        // Integer Data Type Example
        int myIntt = 12;  // Stores whole numbers
        System.out.println("Integer value: " + myIntt);

        // Short Data Type Example
        short myShortt;
        myShortt = 455;  // Stores smaller whole numbers
        System.out.println("Short value (first): " + myShortt);
        myShortt = 555;
        System.out.println("Short value (updated): " + myShortt);

        // Boolean Data Type Example
        boolean myBooleann;
        myBooleann = false;  // Stores true/false values
        System.out.println("Boolean value: " + myBooleann);

        // Float Data Type Example
        float myFloatt;
        myFloatt = 5.0F;  // Stores decimal numbers with less precision
        System.out.println("Float value: " + myFloatt);

        // Double Data Type Example
        double myDouble;
        myDouble = 5.0d;  // Stores decimal numbers with higher precision
        System.out.println("Double value: " + myDouble);

        // String Data Type Example
        String myString;
        myString = "Good Morning";  // Stores sequence of characters
        System.out.println("String value: " + myString);
    }
}
